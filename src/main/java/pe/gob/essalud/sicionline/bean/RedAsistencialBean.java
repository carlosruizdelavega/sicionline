package pe.gob.essalud.sicionline.bean;

public class RedAsistencialBean {
	
	private String idRedAsistencial, nomRedAsistencial, esRedAsistencial, idCrea, feCrea, idModi, feModi;

	public String getIdRedAsistencial() {
		return idRedAsistencial;
	}

	public void setIdRedAsistencial(String idRedAsistencial) {
		this.idRedAsistencial = idRedAsistencial;
	}

	public String getNomRedAsistencial() {
		return nomRedAsistencial;
	}

	public void setNomRedAsistencial(String nomRedAsistencial) {
		this.nomRedAsistencial = nomRedAsistencial;
	}

	public String getEsRedAsistencial() {
		return esRedAsistencial;
	}

	public void setEsRedAsistencial(String esRedAsistencial) {
		this.esRedAsistencial = esRedAsistencial;
	}

	public String getIdCrea() {
		return idCrea;
	}

	public void setIdCrea(String idCrea) {
		this.idCrea = idCrea;
	}

	public String getFeCrea() {
		return feCrea;
	}

	public void setFeCrea(String feCrea) {
		this.feCrea = feCrea;
	}

	public String getIdModi() {
		return idModi;
	}

	public void setIdModi(String idModi) {
		this.idModi = idModi;
	}

	public String getFeModi() {
		return feModi;
	}

	public void setFeModi(String feModi) {
		this.feModi = feModi;
	}

}
