package pe.gob.essalud.sicionline.bean;

public class TurnoBean {
	
	private String idTurno, desTurno, idCrea, feCrea, idModi, feModi;

	public String getIdTurno() {
		return idTurno;
	}

	public void setIdTurno(String idTurno) {
		this.idTurno = idTurno;
	}

	public String getDesTurno() {
		return desTurno;
	}

	public void setDesTurno(String desTurno) {
		this.desTurno = desTurno;
	}

	public String getIdCrea() {
		return idCrea;
	}

	public void setIdCrea(String idCrea) {
		this.idCrea = idCrea;
	}

	public String getFeCrea() {
		return feCrea;
	}

	public void setFeCrea(String feCrea) {
		this.feCrea = feCrea;
	}

	public String getIdModi() {
		return idModi;
	}

	public void setIdModi(String idModi) {
		this.idModi = idModi;
	}

	public String getFeModi() {
		return feModi;
	}

	public void setFeModi(String feModi) {
		this.feModi = feModi;
	}

}
