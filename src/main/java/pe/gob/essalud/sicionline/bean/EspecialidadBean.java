package pe.gob.essalud.sicionline.bean;

public class EspecialidadBean {
	
	private String idEspecialidad, nomEspecialidad, idCrea, feCrea, idModi, feModi;

	public String getIdEspecialidad() {
		return idEspecialidad;
	}

	public void setIdEspecialidad(String idEspecialidad) {
		this.idEspecialidad = idEspecialidad;
	}

	public String getNomEspecialidad() {
		return nomEspecialidad;
	}

	public void setNomEspecialidad(String nomEspecialidad) {
		this.nomEspecialidad = nomEspecialidad;
	}

	public String getIdCrea() {
		return idCrea;
	}

	public void setIdCrea(String idCrea) {
		this.idCrea = idCrea;
	}

	public String getFeCrea() {
		return feCrea;
	}

	public void setFeCrea(String feCrea) {
		this.feCrea = feCrea;
	}

	public String getIdModi() {
		return idModi;
	}

	public void setIdModi(String idModi) {
		this.idModi = idModi;
	}

	public String getFeModi() {
		return feModi;
	}

	public void setFeModi(String feModi) {
		this.feModi = feModi;
	}

}
