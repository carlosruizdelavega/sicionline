package pe.gob.essalud.sicionline.bean;

public class HorarioBean {
	
	private String idHorario, hInicio, hFin, estado, idCrea, feCrea, idModi, feModi;

	public String getIdHorario() {
		return idHorario;
	}

	public void setIdHorario(String idHorario) {
		this.idHorario = idHorario;
	}

	public String gethInicio() {
		return hInicio;
	}

	public void sethInicio(String hInicio) {
		this.hInicio = hInicio;
	}

	public String gethFin() {
		return hFin;
	}

	public void sethFin(String hFin) {
		this.hFin = hFin;
	}

	public String getEstado() {
		return estado;
	}

	public void setEstado(String estado) {
		this.estado = estado;
	}

	public String getIdCrea() {
		return idCrea;
	}

	public void setIdCrea(String idCrea) {
		this.idCrea = idCrea;
	}

	public String getFeCrea() {
		return feCrea;
	}

	public void setFeCrea(String feCrea) {
		this.feCrea = feCrea;
	}

	public String getIdModi() {
		return idModi;
	}

	public void setIdModi(String idModi) {
		this.idModi = idModi;
	}

	public String getFeModi() {
		return feModi;
	}

	public void setFeModi(String feModi) {
		this.feModi = feModi;
	}

}
