package pe.gob.essalud.sicionline.bean;

public class MedicoBean {
	
	private String idMedico, nomMedico, idCrea, feCrea, idModi, feModi;

	public String getIdMedico() {
		return idMedico;
	}

	public void setIdMedico(String idMedico) {
		this.idMedico = idMedico;
	}

	public String getNomMedico() {
		return nomMedico;
	}

	public void setNomMedico(String nomMedico) {
		this.nomMedico = nomMedico;
	}

	public String getIdCrea() {
		return idCrea;
	}

	public void setIdCrea(String idCrea) {
		this.idCrea = idCrea;
	}

	public String getFeCrea() {
		return feCrea;
	}

	public void setFeCrea(String feCrea) {
		this.feCrea = feCrea;
	}

	public String getIdModi() {
		return idModi;
	}

	public void setIdModi(String idModi) {
		this.idModi = idModi;
	}

	public String getFeModi() {
		return feModi;
	}

	public void setFeModi(String feModi) {
		this.feModi = feModi;
	}

}
